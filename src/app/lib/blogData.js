// Helper functions for blog data
export function formatDateGerman(dateString) {
  const date = new Date(dateString);
  const options = { day: 'numeric', month: 'long', year: 'numeric' };
  return date.toLocaleDateString('de-DE', options);
}

export function getAllPosts() {
  return blogPosts.sort((a, b) => new Date(b.date) - new Date(a.date));
}

export function getPostBySlug(slug) {
  return blogPosts.find(post => post.slug === slug);
}

export function getPostsByCategory(category) {
  return blogPosts.filter(post => 
    post.category.toLowerCase().replace(/\s+/g, '-').replace(/&/g, '') === category
  );
}

export function getRecentPosts(limit = 4) {
  return getAllPosts().slice(0, limit);
}

export function getCategories() {
  const categories = [...new Set(blogPosts.map(post => post.category))];
  return categories.map(category => ({
    name: category,
    slug: category.toLowerCase().replace(/\s+/g, '-').replace(/&/g, ''),
    count: blogPosts.filter(post => post.category === category).length
  }));
}

